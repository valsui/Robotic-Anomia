//grabs column from the matrix 
export const grabCol = (k, matrix) => {
    let col = [];
    for (let i = 0 ; i < matrix.length; i++){
        col.push(matrix[i][k]);
    }
    return col;
}

//performs the weighted sum 
export const multiplyWeights = (weights, pix) => {
    let sum = 0;
    for(let i = 0; i < weights.length; i++){
        if(pix === 0){
            continue;
        }else{
            sum += weights[i]*pix;
        }
    };
    return Math.floor(sum);
}

//maps the pixels to a matrix
export const imageToMatrix = (imageArray) => {
    let matrix = []
    const sliceAt = Math.floor(Math.sqrt(imageArray.length));
    for (let i = 0; i < sliceAt-1; i++){
        let row = imageArray.slice(i*sliceAt, (i+1)*sliceAt);
        matrix.push(row);
    }
    return matrix;
}

// maps each pixel in the image to its weigthted sum
export const getImageMapping = (input,weightMatrix) => {
    let image = [];
    for(let i = 0; i <weightMatrix[0].length; i++){
        let col = grabCol(i, weightMatrix);
        // debugger;
        let weightedPixel = multiplyWeights(col,input[i]);
        // debugger;
        image.push(weightedPixel);
    }
    return imageToMatrix(image);
}
